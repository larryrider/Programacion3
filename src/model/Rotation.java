package model;

/**
 * Clase Rotation: Representa los cuatro valores posibles de grados de rotacion
 * de una pieza.
 *
 * @author Lawrence Arthur Rider Garcia DNI: ********-
 */
public enum Rotation {
	
	/**
	 * Rotacion D0
	 * 
	 */
	D0,
	/**
	 * Rotacion D270
	 * 
	 */
	D270,
	/**
	 * Rotacion D180
	 * 
	 */
	D180,
	/**
	 * Rotacion D90
	 * 
	 */
	D90
}
